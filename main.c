#include <stdio.h>
#include <ctype.h>

int	ft_isalpha(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}

int	main(void)
{
	int	i;

	i = 'P';
	printf("%d\n", ft_isalpha(i));
	return (0);
}
